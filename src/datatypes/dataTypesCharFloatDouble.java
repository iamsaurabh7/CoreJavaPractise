package datatypes;

public class dataTypesCharFloatDouble {
    public static void main(String[] args) {
        Character myChar;
        float myFloat;
        double myDouble;

        myChar = 'A';
        System.out.println(myChar);

        myChar = 65;
        System.out.println(myChar);

        myFloat = 22.7F;
        System.out.println(myFloat);

        myFloat = (float) 50 / 3;
        System.out.println(myFloat);

        myDouble = -0.676473453;
        System.out.println(myDouble);

        myDouble = (double) 220 / 7;
        System.out.println(myDouble);
    }
}
